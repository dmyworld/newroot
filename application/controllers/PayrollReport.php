    public function view_payslip() {
        $id = $this->input->get('id');
        $download = $this->input->get('d');
        
        $data['payslip'] = $this->reports->get_employee_payslip($id);
        
        if(!$data['payslip']) {
            echo 'Payslip not found or invalid ID.';
            return;
        }
        
        // Security Check
        $current_user_id = $this->aauth->get_user()->id;
        $payslip_employee_id = $data['payslip']['employee_id'];
        
        // Allow if Own Payslip OR Admin/Manager (Role > 3? Check implementation rules, usually roleid 5=Owner, 4=BizManager)
        // Let's assume permission(14) is HR/Payroll Admin.
        if($payslip_employee_id != $current_user_id) {
            if(!$this->aauth->premission(14) && $this->aauth->get_user()->roleid < 4) {
                 exit('<h3>Access Denied: You are not authorized to view this payslip.</h3>');
            }
        }

        $data['details'] = json_decode($data['payslip']['deduction_details'], true);
        if(!$data['details']) $data['details'] = array();
        
        // Fetch Extended Stats (OT & Jobs)
        $data['stats'] = $this->reports->get_payslip_statistics(
            $data['payslip']['employee_id'], 
            $data['payslip']['start_date'], 
            $data['payslip']['end_date']
        );
        
        // Load View as HTML
        $html = $this->load->view('payroll/report/payslip_view', $data, true);
        
        // If HTML requested or PDF not available, show HTML
        if($download == 'html' || !$download) {
            echo $html;
            return;
        }
        
        // Load PDF Library for download
        try {
            $this->load->library('pdf');
            $pdf = $this->pdf->load();
            $pdf->SetHTMLFooter('<div style="text-align: right;font-family: serif; font-size: 8pt; color: #5C5C5C; font-style: italic;margin-top:-6pt;">Generated by GeoPOS Payroll</div>');
            $pdf->WriteHTML($html);
            
            $filename = 'Payslip_' . $data['payslip']['name'] . '_' . $data['payslip']['run_id'] . '.pdf';
            $pdf->Output($filename, $download == '1' ? 'D' : 'I');
        } catch (Exception $e) {
            // If PDF fails, show HTML
            echo $html;
        }
    }
    public function export_run() {
         $run_id = $this->input->get('id');
         $items = $this->reports->get_payroll_items($run_id);
         
         header('Content-Type: text/csv');
         header('Content-Disposition: attachment; filename="payroll_run_' . $run_id . '.csv"');
         
         $fp = fopen('php://output', 'w');
         fputcsv($fp, array('Employee ID', 'Name', 'Bank Name', 'Bank Account', 'Gross Pay', 'Tax', 'Deductions', 'Net Pay'));
         
         foreach ($items as $row) {
             // Accessing DB directly here for speed as model update is separate
             $emp = $this->db->select('bank_name, bank_ac')->where('id', $row['employee_id'])->get('geopos_employees')->row_array();
             
             fputcsv($fp, array(
                 $row['employee_id'], 
                 $row['name'], 
                 isset($emp['bank_name']) ? $emp['bank_name'] : '',
                 isset($emp['bank_ac']) ? $emp['bank_ac'] : '',
                 $row['gross_pay'], 
                 $row['tax'], 
                 $row['total_deductions'], 
                 $row['net_pay']
             ));
         }
         fclose($fp);
    }

