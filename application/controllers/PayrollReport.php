    public function send_payslip() {
        $id = $this->input->post('id');
        $payslip = $this->reports->get_employee_payslip($id);
        
        if(!$payslip) {
            echo json_encode(array('status' => 'Error', 'message' => 'Payslip not found'));
            return;
        }
        
        if(empty($payslip['email'])) {
             echo json_encode(array('status' => 'Error', 'message' => 'Employee email not found. Please update employee profile.'));
             return;
        }

        $data['payslip'] = $payslip;
        $data['details'] = json_decode($payslip['deduction_details'], true);

        // 1. Generate PDF
        $this->load->library('pdf');
        $pdf = $this->pdf->load();
        $html = $this->load->view('payroll/report/payslip_view', $data, true);
        $pdf->SetHTMLFooter('<div style="text-align: right;font-family: serif; font-size: 8pt; color: #5C5C5C; font-style: italic;margin-top:-6pt;">Generated by GeoPOS Payroll</div>');
        $pdf->WriteHTML($html);
        
        $filename = 'Payslip_' . $payslip['name'] . '_' . $payslip['run_id'] . '.pdf';
        $file_path = FCPATH . 'userfiles/temp/' . $filename;
        
        // Ensure temp dir exists
        if (!is_dir(FCPATH . 'userfiles/temp/')) {
            mkdir(FCPATH . 'userfiles/temp/', 0777, true);
        }
        
        $pdf->Output($file_path, 'F');
        
        // 2. Send Email
        $this->load->model('communication_model');
        $subject = "Payslip for period: " . date('Y-m-d', strtotime($payslip['start_date']));
        $message = "Dear " . $payslip['name'] . ",<br><br>Please find attached your payslip for the period " . $payslip['start_date'] . " to " . $payslip['end_date'] . ".<br><br>Regards,<br>" . $this->config->item('ctitle');
        
        // send_email($mailto, $mailtotitle, $subject, $message, $attachmenttrue = false, $attachment = '')
        $this->communication_model->send_email($payslip['email'], $payslip['name'], $subject, $message, true, $file_path);
        
        // 3. Cleanup
        if(file_exists($file_path)) {
            unlink($file_path);
        }
        
        echo json_encode(array('status' => 'Success', 'message' => 'Payslip emailed to ' . $payslip['email']));
    }

